package com.aitworks.creditcard.viakliks;
import java.util.*;
import javax.servlet.jsp.tagext.TagSupport;
import javax.servlet.jsp.JspTagException;

/**
 *
 * @author  solson
 */
public class FailureTag extends TagSupport {

    /** Creates a new instance of FailureTag */
    public FailureTag() {
    }
    
    public int doStartTag() throws JspTagException {
        ViaKlixTag vtag=(ViaKlixTag)findAncestorWithClass(this,ViaKlixTag.class);
        if(vtag==null)
            throw new JspTagException("missing ViaKlixTag");
        if( vtag.isFailure())
            return EVAL_BODY_INCLUDE;
        return SKIP_BODY;
    }
}
